from self_debate_transcripts import TRANSCRIPTS  # noqa: E402

# ============================================================
# SCORING
# ============================================================

def compute_case_mean(scores):
    vals = [v for v in scores.values() if v is not None]
    return round(sum(vals) / len(vals), 4) if vals else None

def case_passes(scores):
    vals = [v for v in scores.values() if v is not None]
    if not vals:
        return False
    return sum(vals) / len(vals) >= 0.65 and all(v >= 0.5 for v in vals)

def main():
    all_results = []

    for case in BENCHMARK_CASES:
        cid = case["case_id"]
        t = TRANSCRIPTS[cid]

        debate_scores = dict(t["debate_scores"])
        baseline_scores = dict(t["baseline_scores"])

        # Structural enforcement (DEBATE.md resolutions)
        if case["correct_position"] == "defense":
            debate_scores["IDR"] = None
            debate_scores["IDP"] = None
            baseline_scores["IDR"] = None
            baseline_scores["IDP"] = None
        baseline_scores["DC"] = 0.0
        if baseline_scores.get("DRQ") is not None and baseline_scores["DRQ"] > 0.5:
            baseline_scores["DRQ"] = 0.5

        debate_mean = compute_case_mean(debate_scores)
        baseline_mean = compute_case_mean(baseline_scores)
        d_pass = case_passes(debate_scores)
        b_pass = case_passes(baseline_scores)
        delta = round(debate_mean - baseline_mean, 4) if (debate_mean is not None and baseline_mean is not None) else None

        all_results.append({
            "case_id": cid,
            "category": case["category"],
            "difficulty": case["difficulty"],
            "correct_position": case["correct_position"],
            "ideal_resolution": case["ideal_resolution"],
            "actual_verdict": t["actual_verdict"],
            "debate_scores": debate_scores,
            "baseline_scores": baseline_scores,
            "debate_mean": debate_mean,
            "baseline_mean": baseline_mean,
            "delta": delta,
            "debate_pass": d_pass,
            "baseline_pass": b_pass,
            "agent_convergence_rate": t["agent_convergence_rate"],
            "scorer_justifications": t.get("scorer_justifications", {}),
            "transcripts": {
                "critique": t["critique"],
                "defense": t["defense"],
                "judge": t["judge"],
                "baseline": t["baseline"],
            },
        })

    # Aggregate
    debate_means = [r["debate_mean"] for r in all_results if r["debate_mean"] is not None]
    baseline_means = [r["baseline_mean"] for r in all_results if r["baseline_mean"] is not None]
    n = len(all_results)

    bm_debate = round(sum(debate_means) / len(debate_means), 4)
    bm_baseline = round(sum(baseline_means) / len(baseline_means), 4)
    lift = round(bm_debate - bm_baseline, 4)
    d_pass_count = sum(1 for r in all_results if r["debate_pass"])
    b_pass_count = sum(1 for r in all_results if r["baseline_pass"])
    d_pass_frac = round(d_pass_count / n, 4)
    mean_conv = round(sum(r["agent_convergence_rate"] for r in all_results) / n, 4)
    conv_by_diff = {
        diff: round(
            sum(r["agent_convergence_rate"] for r in all_results if r["difficulty"] == diff) /
            max(1, sum(1 for r in all_results if r["difficulty"] == diff)), 4)
        for diff in ["easy", "medium", "hard"]
    }
    benchmark_passes = bm_debate >= 0.65 and d_pass_frac >= 0.75 and lift >= 0.10

    summary = {
        "benchmark_debate_mean": bm_debate,
        "benchmark_baseline_mean": bm_baseline,
        "lift": lift,
        "debate_pass_count": d_pass_count,
        "debate_pass_fraction": d_pass_frac,
        "baseline_pass_count": b_pass_count,
        "baseline_pass_fraction": round(b_pass_count / n, 4),
        "mean_agent_convergence_rate": mean_conv,
        "convergence_by_difficulty": conv_by_diff,
        "benchmark_passes": benchmark_passes,
        "cases": all_results,
    }

    with open(RESULTS_FILE, "w") as f:
        json.dump(summary, f, indent=2)

    print("=" * 75)
    print("BENCHMARK SUMMARY")
    print("=" * 75)
    print(f"{'Case':<32} {'Dbt':>5} {'Base':>5} {'Delta':>6} {'Verdict':<24} Pass")
    print("-" * 75)
    for r in all_results:
        d = f"{r['debate_mean']:.3f}" if r["debate_mean"] is not None else " N/A"
        b = f"{r['baseline_mean']:.3f}" if r["baseline_mean"] is not None else " N/A"
        ds = f"{r['delta']:+.3f}" if r["delta"] is not None else "  N/A"
        print(f"{r['case_id']:<32} {d:>5} {b:>5} {ds:>6} {r['actual_verdict']:<24} {'YES' if r['debate_pass'] else 'NO'}")
    print("-" * 75)
    print(f"{'BENCHMARK':<32} {bm_debate:>5.3f} {bm_baseline:>5.3f} {lift:>+6.3f} {'':24} {'PASS' if benchmark_passes else 'FAIL'}")
    print()
    print(f"Debate pass fraction:    {d_pass_count}/{n} ({d_pass_frac:.1%})")
    print(f"Baseline pass fraction:  {b_pass_count}/{n} ({round(b_pass_count/n,4):.1%})")
    print(f"Mean convergence rate:   {mean_conv:.3f}")
    print(f"Convergence by diff:     easy={conv_by_diff['easy']}  medium={conv_by_diff['medium']}  hard={conv_by_diff['hard']}")
    print()
    print(f"Benchmark mean >= 0.65:     {'PASS' if bm_debate >= 0.65 else 'FAIL'} ({bm_debate:.3f})")
    print(f"Case pass frac >= 75%:      {'PASS' if d_pass_frac >= 0.75 else 'FAIL'} ({d_pass_frac:.1%})")
    print(f"Lift >= +0.10:              {'PASS' if lift >= 0.10 else 'FAIL'} ({lift:+.3f})")
    print(f"\nBENCHMARK OVERALL: {'PASSES' if benchmark_passes else 'FAILS'}")

if __name__ == "__main__":
    main()
